package Lesson01;

public class DataTypesNonePrimitives04 {
    public static void main(String[] args) {

        String [][] array = new String[2][3];
        array [0][0]  = "Moshe";
        array [0][1] = "22";
        array [0][2] = "Jerusalem";
        array [1][0] = "Joseph";
        array [1][1] = "45";
        array [1][2] = "London";
        System.out.println(array[0][2]);
    }
}
